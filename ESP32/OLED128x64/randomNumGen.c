void mostrarImagenInicial()
{
    display.clearDisplay();
    display.drawBitmap(0, 0, EscudoIPN, 128, 64, WHITE);
    display.display();
    delay(2000); // Mostrar imagen por 2 segundos
}

void generarNumerosUnicos(int *numeros, int cantidad, int rangoMin, int rangoMax)
{
    int indice = 0;
    while (indice < cantidad)
    {
        int nuevoNumero = random(rangoMin, rangoMax + 1);
        bool repetido = false;
        for (int i = 0; i < indice; i++)
        {
            if (numeros[i] == nuevoNumero)
            {
                repetido = true;
                break;
            }
        }
        if (!repetido)
        {
            numeros[indice] = nuevoNumero;
            indice++;
        }
    }
}

void ordenarNumeros(int *numeros, int cantidad)
{
    for (int i = 0; i < cantidad - 1; i++)
    {
        for (int j = 0; j < cantidad - i - 1; j++)
        {
            if (numeros[j] > numeros[j + 1])
            {
                int temp = numeros[j];
                numeros[j] = numeros[j + 1];
                numeros[j + 1] = temp;
            }
        }
    }
}

void setup()
{
    pinMode(PUSH_BUTTON, INPUT_PULLUP);
    Serial.begin(9600);

    if (!display.begin(SSD1306_SWITCHCAPVCC, 0x3C))
    {
        Serial.println("No se encuentra la pantalla OLED");
        while (true)
            ;
    }
    randomSeed(analogRead(36));
    mostrarImagenInicial();

    display.clearDisplay();
    display.setTextSize(1);
    display.setTextColor(SSD1306_WHITE);
    display.setCursor(0, 10);
    display.println("Presiona el boton");
    display.println("para iniciar...");
    display.display();
}

void loop()
{
    if (digitalRead(PUSH_BUTTON) == LOW)
    {
        delay(40); // Antirrebotes
        if (digitalRead(PUSH_BUTTON) == LOW)
        {
            display.clearDisplay();

            int numeros[6];
            generarNumerosUnicos(numeros, 6, 1, 57);
            ordenarNumeros(numeros, 6);

            display.setTextSize(1);
            display.setCursor(0, 10);
            display.println("Numeros generados:");
            display.setCursor(0, 30);
            for (int i = 0; i < 6; i++)
            {
                display.print(numeros[i]);
                if (i < 5)
                {
                    display.print(", ");
                }
            }
            display.display();
        }
    }
}
